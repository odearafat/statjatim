package com.example.bottommenu.model;

public class Page{

    int page, pages, pagesper_page, count, total;

    public int getPage() {
        return page;
    }

    public int getPages() { return pages; }

    public int getPagesper_page() {
        return pagesper_page;
    }

    public int getCount() {
        return count;
    }

    public int getTotal() {
        return total;
    }
}


