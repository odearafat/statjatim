public class InfografisItem {

    private Integer inf_id;
    private String title;
    private String img;
    private String desc;
    private Integer category;
    private String dl;

    public Integer getInf_id() {
        return inf_id;
    }

    public String getTitle() {
        return title;
    }

    public String getImg() {
        return img;
    }

    public String getSch_date() {
        return desc;
    }

    public Integer getRl_date() {
        return category;
    }

    public String getDl() {
        return dl;
    }
}

